function normalizeToolCreationRequest(requestOrRequester, maybeToolSpecs = [], metadata = {}) {
  if (typeof requestOrRequester === "string") {
    return {
      requester: requestOrRequester,
      metadata: { ...metadata },
      tool_specs: Array.isArray(maybeToolSpecs) ? maybeToolSpecs : []
    };
  }

  const request = isPlainObject(requestOrRequester) ? requestOrRequester : {};
  return {
    requester: request.requester || request.sender || null,
    metadata: { ...(request.metadata || {}) },
    tool_specs: Array.isArray(request.tool_specs)
      ? request.tool_specs
      : Array.isArray(request.toolSpecs)
        ? request.toolSpecs
        : []
  };
}

function normalizeToolSpec(spec = {}, requestMetadata = {}) {
  const runtime = chooseRuntime(spec);
  const goal = String(spec.goal || spec.description || spec.name || "Generated tool").trim();
  const target = isPlainObject(spec.target) ? spec.target : {};
  const constraints = normalizeList(spec.constraints);
  const version = String(spec.version || "1.0.0");
  const baseToolId = spec.base_tool_id || spec.id || spec.tool_id || makeToolId("tool", `${goal}:${target.url || target.title || runtime}`);
  const toolId = spec.tool_id || spec.id || `${baseToolId}_v${version.replace(/\./g, "_")}`;
  const strategy = spec.strategy || detectStrategy(spec);

  return {
    tool_id: toolId,
    id: toolId,
    base_tool_id: baseToolId,
    version,
    name: spec.name || `Tool ${baseToolId}`,
    description: spec.description || goal,
    requester: requestMetadata.requester || spec.requester || null,
    request_id: requestMetadata.request_id || spec.request_id || null,
    created_for: requestMetadata.requester || spec.created_for || null,
    goal,
    target,
    constraints,
    runtime,
    strategy,
    synthesis_mode: spec.synthesis_mode || ((spec.code || spec.implementation) ? "creator_agent" : "heuristic"),
    input_schema: spec.input_schema || {
      type: "object",
      required: (spec.parameters || []).some((item) => item.name === "candidate") ? ["candidate"] : ["target"]
    },
    output_schema: spec.output_schema || ((spec.parameters || []).some((item) => item.name === "candidate")
      ? {
        type: "object",
        kind: "normalized_read",
        required: ["source_id", "title", "content_type"]
      }
      : {
        type: "object",
        kind: "tool_result",
        required: ["success"]
      }),
    site_scope: spec.site_scope || target.url || target.platform || "global",
    safety_level: spec.safety_level || "restricted",
    implementation_plan: spec.implementation_plan || {
      runtime,
      strategy,
      goal,
      constraints,
      reusable: true
    },
    code: typeof spec.code === "string" && spec.code.trim()
      ? spec.code
      : buildGeneratedCode({ ...spec, runtime, strategy, name: spec.name || `Tool ${baseToolId}` }),
    implementation: typeof spec.implementation === "function"
      ? spec.implementation
      : (typeof spec.execute === "function" ? spec.execute : null),
    parameters: Array.isArray(spec.parameters) ? spec.parameters : [],
    lifecycle_state: spec.lifecycle_state || "ephemeral",
    source: spec.source || "dynamic",
    auto_promote: spec.auto_promote !== false,
    validation_profile: spec.validation_profile || "strict",
    quality_gate: spec.quality_gate || "strict"
  };
}

function detectNodePolicyViolations(code, sandbox) {
  const violations = [];
  const source = String(code || "");
  if (/require\(["']child_process["']\)|require\(["']worker_threads["']\)|require\(["']cluster["']\)/.test(source)) {
    violations.push("process_spawning_not_allowed");
  }
  if (/fs\.(writeFile|writeFileSync|appendFile|appendFileSync|rm|rmSync|unlink|unlinkSync|rename|renameSync|mkdir|mkdirSync)\s*\(/.test(source)
    && sandbox.filesystem !== "temp-only") {
    violations.push("filesystem_write_requires_temp_only_policy");
  }
  if (/\bfetch\s*\(|require\(["']https?["']\)|https?\./.test(source) && sandbox.network !== true) {
    violations.push("network_access_blocked");
  }
  if (/process\.env|require\(["']os["']\)/.test(source)) {
    violations.push("host_introspection_not_allowed");
  }
  return violations;
}

function detectPythonPolicyViolations(code, sandbox) {
  const violations = [];
  const source = String(code || "");
  if (/\bimport\s+subprocess\b|\bfrom\s+subprocess\s+import\b|\bos\.system\(/.test(source)) {
    violations.push("process_spawning_not_allowed");
  }
  if (/\bopen\s*\(.+,\s*["'][wa+]/.test(source) && sandbox.filesystem !== "temp-only") {
    violations.push("filesystem_write_requires_temp_only_policy");
  }
  if (/\bimport\s+requests\b|\bimport\s+socket\b|\burllib\b/.test(source) && sandbox.network !== true) {
    violations.push("network_access_blocked");
  }
  if (/\bimport\s+os\b|\bos\.environ/.test(source)) {
    violations.push("host_introspection_not_allowed");
  }
  return violations;
}

function normalizeSandboxPolicy(sandbox = {}) {
  return {
    network: sandbox.network === true,
    filesystem: sandbox.filesystem || DEFAULT_SANDBOX_POLICY.filesystem,
    timeout_ms: Number(sandbox.timeout_ms || DEFAULT_SANDBOX_POLICY.timeout_ms),
    max_output_bytes: Number(sandbox.max_output_bytes || DEFAULT_SANDBOX_POLICY.max_output_bytes)
  };
}

function enforceSandboxPolicy(executable, sandbox = {}) {
  const policy = normalizeSandboxPolicy(sandbox);
  if (executable.implementation) {
    return policy;
  }
  const violations = executable.runtime === "python"
    ? detectPythonPolicyViolations(executable.code, policy)
    : detectNodePolicyViolations(executable.code, policy);
  if (violations.length > 0) {
    const error = new Error(`Sandbox policy rejected tool: ${violations.join(", ")}`);
    error.code = "sandbox_policy_violation";
    error.violations = violations;
    throw error;
  }
  return policy;
}

function qualityGateToolSpec(spec) {
  const reasons = [];
  if (!spec.name) {
    reasons.push("missing_name");
  }
  if (!spec.goal && !spec.description) {
    reasons.push("missing_goal");
  }
  if (!spec.runtime || !["node", "python"].includes(spec.runtime)) {
    reasons.push("unsupported_runtime");
  }
  if (!spec.implementation && !(typeof spec.code === "string" && spec.code.trim())) {
    reasons.push("missing_implementation");
  }
  if (!spec.output_schema || !Array.isArray(spec.output_schema.required)) {
    reasons.push("missing_output_schema");
  }
  if ((spec.quality_gate || "strict") === "strict" && !Array.isArray(spec.parameters)) {
    reasons.push("missing_parameters");
  }
  return {
    success: reasons.length === 0,
    reasons
  };
}

function createVersionString(baseToolId, explicitVersion = null) {
  if (explicitVersion) {
    return String(explicitVersion);
  }
  const history = ToolRegistry.getToolHistory(baseToolId);
  if (!history.length) {
    return "1.0.0";
  }
  const latest = history[history.length - 1].version || "1.0.0";
  const parts = latest.split(".").map((item) => Number(item) || 0);
  parts[2] += 1;
  return parts.join(".");
}

function versionedToolSpec(spec, requestMetadata = {}) {
  const baseToolId = spec.base_tool_id || spec.id || spec.tool_id || makeToolId("tool", spec.name || spec.goal || Date.now());
  const version = createVersionString(baseToolId, spec.version);
  return normalizeToolSpec({
    ...spec,
    base_tool_id: baseToolId,
    version,
    tool_id: spec.tool_id || spec.id || `${baseToolId}_v${version.replace(/\./g, "_")}`
  }, requestMetadata);
}

function compileTool(toolSpec) {
  const tool = normalizeToolSpec(toolSpec);
  return {
    ...tool,
    executable_kind: tool.implementation ? "function" : "script",
    code_hash: hashValue(tool.implementation ? tool.implementation.toString() : tool.code),
    spec_hash: hashValue(JSON.stringify({
      tool_id: tool.tool_id,
      base_tool_id: tool.base_tool_id,
      runtime: tool.runtime,
      strategy: tool.strategy,
      goal: tool.goal
    })),
    compiled_at: new Date().toISOString()
  };
}

function buildValidation(toolOrRequest, resultArg, validationContextArg = {}) {
  const request = isPlainObject(toolOrRequest) && (toolOrRequest.tool || toolOrRequest.result || toolOrRequest.validationContext)
    ? toolOrRequest
    : { tool: toolOrRequest, result: resultArg, validationContext: validationContextArg };
  const tool = request.tool;
  const result = request.result;
  const validationContext = request.validationContext || {};
  const payload = result?.output && isPlainObject(result.output)
    ? result.output
    : result?.extracted_data && isPlainObject(result.extracted_data)
      ? result.extracted_data
      : null;
  const reasons = [];
  if (!result?.success) {
    reasons.push(result?.error || "Tool execution failed");
  }
  const required = Array.isArray(tool.output_schema?.required) ? tool.output_schema.required : [];
  if (payload) {
    const missing = required.filter((key) => !Object.prototype.hasOwnProperty.call(payload, key));
    if (missing.length > 0) {
      reasons.push(`Missing required output fields: ${missing.join(", ")}`);
    }
  } else {
    reasons.push("No structured output was produced");
  }
  if ((validationContext.expect_read || tool.output_schema?.kind === "normalized_read") && payload) {
    if (!payload.source_id || !payload.title || !(payload.content_type || payload.source_type)) {
      reasons.push("Normalized read contract was not satisfied");
    }
  }
  if (validationContext.expected_source_id && payload?.source_id && payload.source_id !== validationContext.expected_source_id) {
    reasons.push(`Expected source_id ${validationContext.expected_source_id} but received ${payload.source_id}`);
  }
  return {
    success: reasons.length === 0,
    agent: "fact_verifier",
    verdict: reasons.length === 0 ? "supported" : "conflicted",
    rejected: reasons.length > 0,
    reasons: reasons.length > 0 ? reasons : ["Output satisfied the requested contract"],
    verified_at: new Date().toISOString()
  };
}

async function runExperimentValidation(tool, validationContext = {}, baseSandbox = {}) {
  const experimentInputs = Array.isArray(validationContext.experiment_inputs) ? validationContext.experiment_inputs : [];
  const experiments = [];
  for (const input of experimentInputs.slice(0, 3)) {
    const run = await executeTool({
      executable: tool,
      input,
      sandbox: {
        ...baseSandbox,
        timeout_ms: Math.min(5000, Number(baseSandbox.timeout_ms || DEFAULT_SANDBOX_POLICY.timeout_ms))
      }
    });
    experiments.push({
      success: run.success,
      result_hash: run.hashes?.result_hash || null,
      error: run.error || null
    });
  }

  let deterministic = null;
  if (validationContext.require_deterministic && validationContext.deterministic_input) {
    const first = await executeTool({
      executable: tool,
      input: validationContext.deterministic_input,
      sandbox: baseSandbox
    });
    const second = await executeTool({
      executable: tool,
      input: validationContext.deterministic_input,
      sandbox: baseSandbox
    });
    deterministic = {
      success: first.success && second.success && first.hashes?.result_hash === second.hashes?.result_hash,
      first_hash: first.hashes?.result_hash || null,
      second_hash: second.hashes?.result_hash || null
    };
  }

  return {
    experiments,
    deterministic
  };
}

async function executeFunctionTool(executable, input, sandbox = {}) {
  const startedAt = Date.now();
  const policy = normalizeSandboxPolicy(sandbox);
  try {
    const output = await executable.implementation(input);
    return {
      success: output?.success !== false,
      tool: executable,
      sandbox: {
        timeout_ms: policy.timeout_ms,
        network: policy.network,
        filesystem: policy.filesystem
      },
      execution: {
        exit_code: 0,
        duration_ms: Date.now() - startedAt,
        stdout: "",
        stderr: "",
        timed_out: false
      },
      logs: Array.isArray(output?.logs) ? output.logs : [],
      output,
      extracted_data: output?.extracted_data || null,
      error: output?.success === false ? output?.error || "tool execution failed" : null,
      error_code: output?.success === false ? "execution_failed" : null,
      hashes: {
        code_hash: executable.code_hash,
        spec_hash: executable.spec_hash,
        input_hash: hashValue(JSON.stringify(input)),
        result_hash: hashValue(JSON.stringify(output || {}))
      },
      lifecycle_state: executable.lifecycle_state || "ephemeral"
    };
  } catch (error) {
    return {
      success: false,
      tool: executable,
      sandbox: {
        timeout_ms: policy.timeout_ms,
        network: policy.network,
        filesystem: policy.filesystem
      },
      execution: {
        exit_code: -1,
        duration_ms: Date.now() - startedAt,
        stdout: "",
        stderr: error.message,
        timed_out: false
      },
      logs: [],
      output: null,
      extracted_data: null,
      error: error.message,
      error_code: "execution_failed",
      hashes: {
        code_hash: executable.code_hash,
        spec_hash: executable.spec_hash,
        input_hash: hashValue(JSON.stringify(input)),
        result_hash: null
      },
      lifecycle_state: executable.lifecycle_state || "ephemeral"
    };
  }
}

function runtimeCommand(runtime) {
  if (runtime === "python") {
    return { command: "python", extension: ".py" };
  }
  return { command: process.execPath, extension: ".js" };
}

async function executeScriptTool(executable, input, sandbox = {}) {
  const policy = enforceSandboxPolicy(executable, sandbox);
  const runtime = runtimeCommand(executable.runtime);
  const tempDir = fs.mkdtempSync(path.join(os.tmpdir(), "opensearch-tool-"));
  const scriptPath = path.join(tempDir, `tool${runtime.extension}`);
  fs.writeFileSync(scriptPath, executable.code);
  const startedAt = Date.now();
  const timeoutMs = policy.timeout_ms;

  const execution = await new Promise((resolve) => {
    const baseEnv = {
      OPENSEARCH_TOOL_TEMP_DIR: tempDir,
      OPENSEARCH_TOOL_NETWORK: policy.network ? "true" : "false",
      OPENSEARCH_TOOL_FILESYSTEM: policy.filesystem
    };
    if (process.env.PATH) {
      baseEnv.PATH = process.env.PATH;
    }
    if (process.env.SystemRoot) {
      baseEnv.SystemRoot = process.env.SystemRoot;
    }
    if (process.env.TMP) {
      baseEnv.TMP = process.env.TMP;
    }
    if (process.env.TEMP) {
      baseEnv.TEMP = process.env.TEMP;
    }
    const child = spawn(runtime.command, [scriptPath], {
      cwd: tempDir,
      env: baseEnv,
      shell: false,
      windowsHide: true
    });
    const stdoutChunks = [];
    const stderrChunks = [];
    let stdoutBytes = 0;
    let stderrBytes = 0;
    let timedOut = false;
    let outputLimitExceeded = false;
    const timer = setTimeout(() => {
      timedOut = true;
      child.kill();
    }, timeoutMs);

    child.stdout.on("data", (chunk) => {
      stdoutBytes += chunk.length;
      if (stdoutBytes > policy.max_output_bytes) {
        outputLimitExceeded = true;
        child.kill();
        return;
      }
      stdoutChunks.push(Buffer.from(chunk));
    });
    child.stderr.on("data", (chunk) => {
      stderrBytes += chunk.length;
      if (stderrBytes > policy.max_output_bytes) {
        outputLimitExceeded = true;
        child.kill();
        return;
      }
      stderrChunks.push(Buffer.from(chunk));
    });
    child.on("error", (error) => {
      clearTimeout(timer);
      resolve({
        exit_code: -1,
        duration_ms: Date.now() - startedAt,
        stdout: "",
        stderr: error.message,
        timed_out: false,
        output_limit_exceeded: false
      });
    });
    child.on("close", (code) => {
      clearTimeout(timer);
      resolve({
        exit_code: code,
        duration_ms: Date.now() - startedAt,
        stdout: Buffer.concat(stdoutChunks).toString("utf8"),
        stderr: Buffer.concat(stderrChunks).toString("utf8"),
        timed_out: timedOut,
        output_limit_exceeded: outputLimitExceeded
      });
    });
    child.stdin.write(JSON.stringify(input));
    child.stdin.end();
  });

  let output = null;
  if (execution.stdout.trim()) {
    try {
      output = JSON.parse(execution.stdout);
    } catch (_) {
      output = null;
    }
  }

  return {
    success: Boolean(output?.success !== false && execution.exit_code === 0 && !execution.timed_out),
    tool: executable,
    sandbox: {
      timeout_ms: timeoutMs,
      network: policy.network,
      filesystem: policy.filesystem,
      max_output_bytes: policy.max_output_bytes,
      temp_dir: tempDir
    },
    execution,
    logs: Array.isArray(output?.logs) ? output.logs : [],
    output,
    extracted_data: output?.extracted_data || null,
    error: output?.error
      || execution.stderr
      || (execution.output_limit_exceeded ? "tool output exceeded sandbox limit" : null)
      || (execution.timed_out ? "tool timed out" : null),
    error_code: execution.output_limit_exceeded
      ? "output_limit_exceeded"
      : execution.timed_out
        ? "timeout"
        : (execution.exit_code === 0 ? null : "execution_failed"),
    hashes: {
      code_hash: executable.code_hash,
      spec_hash: executable.spec_hash,
      input_hash: hashValue(JSON.stringify(input)),
      result_hash: hashValue(JSON.stringify(output || execution.stderr || null))
    },
    lifecycle_state: executable.lifecycle_state || "ephemeral"
  };
}

async function executeTool(request, maybeSandbox = {}) {
  const normalized = isPlainObject(request) && (request.tool || request.executable || request.input || request.sandbox)
    ? request
    : { tool: request, sandbox: maybeSandbox };
  const executable = normalized.executable || compileTool(normalized.tool);
  const sandbox = normalized.sandbox || {};
  const input = normalized.input && isPlainObject(normalized.input)
    ? normalized.input
    : {
      goal: executable.goal,
      target: executable.target,
      constraints: executable.constraints || []
    };

  if (executable.implementation) {
    const result = await executeFunctionTool(executable, input, sandbox);
    appendAuditEvent("tool_execution", {
      tool_id: executable.tool_id,
      runtime: executable.runtime,
      success: result.success,
      error_code: result.error_code || null,
      sandbox: result.sandbox
    });
    return result;
  }
  try {
    const result = await executeScriptTool(executable, input, sandbox);
    appendAuditEvent("tool_execution", {
      tool_id: executable.tool_id,
      runtime: executable.runtime,
      success: result.success,
      error_code: result.error_code || null,
      sandbox: result.sandbox
    });
    return result;
  } catch (error) {
    appendAuditEvent("tool_execution_rejected", {
      tool_id: executable.tool_id,
      runtime: executable.runtime,
      reason: error.message,
      error_code: error.code || "sandbox_policy_violation",
      sandbox: normalizeSandboxPolicy(sandbox)
    });
    throw error;
  }
}

function upsertMemoryEntry(list, predicate, create) {
  const existing = list.find(predicate);
  if (existing) {
    return existing;
  }
  const item = create();
  list.push(item);
  return item;
}

function recordToolOutcome(outcomes, options = {}) {
  const items = Array.isArray(outcomes) ? outcomes : [outcomes];
  const attempts = items.map((item) => item?.result ? {
    ...item.result,
    tool: item.tool || item.result.tool,
    worth_promoting: item.result.worth_promoting
  } : item);
  const memory = readToolMemory(options.memoryPath || toolMemoryPath);
  const merged = recordToolExperience(attempts, options);
  Object.assign(memory, merged);
  memory.tool_versions = memory.tool_versions || [];
  memory.success_rate_by_site = memory.success_rate_by_site || [];
  memory.failure_mode_taxonomy = memory.failure_mode_taxonomy || [];
  memory.verifier_verdicts = memory.verifier_verdicts || [];
  memory.promotion_history = memory.promotion_history || [];
  memory.reuse_history = memory.reuse_history || [];

  for (const item of items) {
    const tool = item?.tool || item?.result?.tool || {};
    const result = item?.result || item;
    const validation = item?.validation || result?.validation || null;
    const toolId = tool.tool_id || tool.id || null;
    if (toolId) {
      const versionEntry = upsertMemoryEntry(
        memory.tool_versions,
        (entry) => entry.tool_id === toolId,
        () => ({
          tool_id: toolId,
          base_tool_id: tool.base_tool_id || toolId,
          version: tool.version || "1.0.0",
          executions: 0,
          verified_successes: 0,
          runtime: tool.runtime || "node",
          lifecycle_state: tool.lifecycle_state || "ephemeral",
          last_seen_at: null
        })
      );
      versionEntry.executions += 1;
      versionEntry.verified_successes += validation?.success ? 1 : 0;
      versionEntry.lifecycle_state = tool.lifecycle_state || versionEntry.lifecycle_state;
      versionEntry.verifier_verdict = validation?.verdict || versionEntry.verifier_verdict || null;
      versionEntry.last_seen_at = new Date().toISOString();
      versionEntry.code_hash = tool.code_hash || versionEntry.code_hash || null;
    }

    const siteKey = tool.site_scope || tool.target?.url || "global";
    const successRate = upsertMemoryEntry(
      memory.success_rate_by_site,
      (entry) => entry.site_key === siteKey && entry.base_tool_id === (tool.base_tool_id || toolId),
      () => ({
        site_key: siteKey,
        base_tool_id: tool.base_tool_id || toolId,
        attempts: 0,
        successes: 0
      })
    );
    successRate.attempts += 1;
    successRate.successes += validation?.success ? 1 : 0;

    if (!result?.success) {
      const failureMode = result?.error_code || result?.error || "execution_failed";
      const failureEntry = upsertMemoryEntry(
        memory.failure_mode_taxonomy,
        (entry) => entry.base_tool_id === (tool.base_tool_id || toolId) && entry.failure_mode === failureMode,
        () => ({
          base_tool_id: tool.base_tool_id || toolId,
          failure_mode: failureMode,
          count: 0
        })
      );
      failureEntry.count += 1;
      failureEntry.last_seen_at = new Date().toISOString();
    }

    if (validation) {
      memory.verifier_verdicts.push({
        tool_id: toolId,
        verdict: validation.verdict,
        success: validation.success,
        verified_at: validation.verified_at,
        reasons: validation.reasons
      });
    }
  }

  writeToolMemory(memory, options.memoryPath || toolMemoryPath);
  appendAuditEvent("tool_outcome_recorded", {
    count: items.length,
    memory_path: options.memoryPath || toolMemoryPath
  });
  return memory;
}

function setRegisteredLifecycle(toolId, lifecycleState, metadata = {}) {
  const tool = ToolRegistry.getTool(toolId);
  if (!tool) {
    return null;
  }
  tool.lifecycle_state = lifecycleState;
  tool.status = lifecycleState === "registered" ? "active" : lifecycleState;
  tool.verifier_verdict = metadata.verifier_verdict || tool.verifier_verdict || null;
  tool.last_verified_at = metadata.last_verified_at || tool.last_verified_at || null;
  tool.promotion_reason = metadata.reason || tool.promotion_reason || null;
  appendAuditEvent("tool_lifecycle_updated", {
    tool_id: tool.id,
    lifecycle_state: lifecycleState,
    verifier_verdict: tool.verifier_verdict || null
  });
  return tool;
}

function maybeRollbackTool(tool, memory) {
  const baseToolId = tool.base_tool_id || tool.tool_id || tool.id;
  const failures = (memory.failure_mode_taxonomy || [])
    .filter((entry) => entry.base_tool_id === baseToolId)
    .reduce((sum, entry) => sum + (entry.count || 0), 0);
  const active = ToolRegistry.getTool(baseToolId);
  if (!active || failures < MAX_FAILURES_BEFORE_ROLLBACK) {
    return null;
  }

  const history = ToolRegistry.getToolHistory(baseToolId).filter((item) => item.id !== active.id && item.status !== "deprecated");
  if (history.length === 0) {
    return null;
  }

  if (active.id !== (tool.tool_id || tool.id) && active.base_tool_id !== baseToolId) {
    return null;
  }

  const rollback = ToolRegistry.rollbackTool(baseToolId);
  appendAuditEvent("tool_rolled_back", {
    base_tool_id: baseToolId,
    from_tool_id: rollback.previous.id,
    to_tool_id: rollback.active.id,
    reason: "failure_threshold_exceeded"
  });
  return rollback;
}

function registerExecutableTool(executable) {
  const existing = ToolRegistry.getTool(executable.tool_id);
  if (existing) {
    existing.lifecycle_state = executable.lifecycle_state || existing.lifecycle_state || "ephemeral";
    existing.status = existing.lifecycle_state === "registered" ? "active" : existing.lifecycle_state;
    existing.runtime = executable.runtime || existing.runtime || "node";
    existing.verifier_verdict = existing.verifier_verdict || null;
    return existing;
  }

  ToolRegistry.registerTool({
    id: executable.tool_id,
    base_tool_id: executable.base_tool_id,
    version: executable.version || "1.0.0",
    name: executable.name,
    description: executable.description,
    parameters: executable.parameters || [],
    inputSchema: executable.input_schema || null,
    outputSchema: executable.output_schema || null,
    runtime: executable.runtime,
    site_scope: executable.site_scope,
    safety_level: executable.safety_level,
    implementation_plan: executable.implementation_plan,
    lifecycle_state: executable.lifecycle_state || "ephemeral",
    created_for: executable.created_for || executable.requester || null,
    request_id: executable.request_id || null,
    execute: async (input) => {
      const result = await executeTool({
        executable,
        input,
        sandbox: { timeout_ms: 15000, network: true }
      });
      if (!result.success) {
        throw new Error(result.error || "tool execution failed");
      }
      return result.output?.extracted_data || result.extracted_data || result.output || {};
    },
    source: executable.source || "dynamic",
    status: executable.lifecycle_state || "ephemeral",
    verifier_verdict: null,
    code_hash: executable.code_hash,
    spec_hash: executable.spec_hash
  });
  return ToolRegistry.getTool(executable.tool_id);
}

function resolveReusableToolCandidate(spec, memory) {
  const preferred = ToolRegistry.getTool(spec.base_tool_id);
  if (preferred && ["candidate", "active", "registered"].includes(preferred.lifecycle_state || preferred.status)) {
    return { reused: true, tool: preferred, reason: "matched_base_tool_id" };
  }
  const history = memory.reuse_history || [];
  const reuseEntry = history.find((entry) =>
    entry.site_scope === spec.site_scope
    && entry.runtime === spec.runtime
    && entry.base_tool_id
  );
  if (reuseEntry) {
    const tool = ToolRegistry.getTool(reuseEntry.base_tool_id);
    if (tool && ["candidate", "active", "registered"].includes(tool.lifecycle_state || tool.status)) {
      return { reused: true, tool, reason: "matched_reuse_memory" };
    }
  }
  return null;
}

function promoteToolCandidate(request) {
  const tool = request.tool;
  const validation = request.validation;
  const memory = request.memory || readToolMemory(request.memoryPath || toolMemoryPath);
  const versionEntry = (memory.tool_versions || []).find((entry) => entry.tool_id === (tool.tool_id || tool.id));
  const successCount = versionEntry?.verified_successes || 0;
  const lifecycleState = !validation?.success
    ? "ephemeral"
    : successCount >= 3 || request.force_register
      ? "registered"
      : successCount >= 2
        ? "candidate"
        : "ephemeral";
  setRegisteredLifecycle(tool.tool_id || tool.id, lifecycleState, {
    reason: validation?.success
      ? `Auto-promoted after ${successCount} verified successes`
      : "Verifier rejected promotion",
    verifier_verdict: validation?.verdict || null,
    last_verified_at: validation?.verified_at || null
  });
  memory.promotion_history = memory.promotion_history || [];
  memory.promotion_history.push({
    tool_id: tool.tool_id || tool.id,
    base_tool_id: tool.base_tool_id || tool.tool_id || tool.id,
    lifecycle_state: lifecycleState,
    promoted: validation?.success === true && lifecycleState !== "ephemeral",
    reason: validation?.success ? `Auto-promoted after ${successCount} verified successes` : "Verifier rejected promotion",
    at: new Date().toISOString()
  });
  if (validation?.success) {
    memory.reuse_history = memory.reuse_history || [];
    memory.reuse_history.push({
      base_tool_id: tool.base_tool_id || tool.tool_id || tool.id,
      site_scope: tool.site_scope || "global",
      runtime: tool.runtime || "node",
      at: new Date().toISOString()
    });
  }
  writeToolMemory(memory, request.memoryPath || toolMemoryPath);
  const rollback = maybeRollbackTool(tool, memory);
  return {
    tool_id: tool.tool_id || tool.id,
    promoted: lifecycleState !== "ephemeral" && validation?.success === true,
    lifecycle_state: lifecycleState,
    reason: validation?.success ? `Auto-promoted after ${successCount} verified successes` : "Tool failed verifier gating",
    updated_at: new Date().toISOString(),
    rollback: rollback ? {
      active: rollback.active.id,
      previous: rollback.previous.id
    } : null
  };
}

async function materializeCreatorOutputs(toolSpecs, creator, requestMetadata) {
  if (!creator || typeof creator.execute !== "function") {
    return toolSpecs.map((spec) => versionedToolSpec(spec, requestMetadata));
  }
  const created = await creator.execute({
    toolCreationRequest: {
      requester: requestMetadata.requester,
      metadata: requestMetadata,
      tool_specs: toolSpecs
    }
  });
  const generated = created?.result?.tools || created?.tools || toolSpecs;
  return generated.map((spec) => versionedToolSpec(spec, requestMetadata));
}

async function requestToolCreation(request, options = {}) {
  const normalized = normalizeToolCreationRequest(request);
  if (normalized.requester && normalized.requester !== "llm_orchestrator") {
    throw new Error("Only LLM-Orchestrator can formally request tool creation");
  }
  const requestMetadata = {
    ...(normalized.metadata || {}),
    requester: normalized.requester || null,
    request_id: normalized.metadata?.request_id || `req_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`
  };
  const memory = readToolMemory(options.memoryPath || toolMemoryPath);
  const materialized = await materializeCreatorOutputs(normalized.tool_specs, options.creator || null, requestMetadata);
  const tools = [];

  for (const spec of materialized) {
    const quality = qualityGateToolSpec(spec);
    if (!quality.success) {
      appendAuditEvent("tool_quality_rejected", {
        tool_id: spec.tool_id || spec.id || null,
        reasons: quality.reasons
      });
      continue;
    }
    const reusable = resolveReusableToolCandidate(spec, memory);
    if (reusable) {
      tools.push({
        ...reusable.tool,
        reused: true,
        reuse_reason: reusable.reason
      });
      continue;
    }
    const executable = compileTool(spec);
    registerExecutableTool(executable);
    appendAuditEvent("tool_registered_ephemeral", {
      tool_id: executable.tool_id,
      runtime: executable.runtime,
      base_tool_id: executable.base_tool_id
    });
    tools.push({
      ...executable,
      lifecycle_state: "ephemeral"
    });
  }

  return {
    request_type: "tool_creation_result",
    success: tools.length > 0,
    count: tools.length,
    request_id: requestMetadata.request_id,
    tools,
    markdown_report: [
      "# Tool Platform Report",
      "",
      ...tools.map((tool) => `- ${tool.name} | ${tool.tool_id || tool.id} | ${tool.runtime || "node"} | ${tool.reused ? "reused" : (tool.lifecycle_state || "ephemeral")}`)
    ].join("\n")
  };
}

async function synthesizeTool(input) {
  const ephemeral = await synthesizeEphemeralTool(input);
  return normalizeToolSpec({
    ...ephemeral,
    name: `Ephemeral ${ephemeral.strategy || "tool"}`,
    description: ephemeral.description,
    strategy: ephemeral.strategy,
    goal: ephemeral.goal,
    target: ephemeral.target,
    constraints: ephemeral.constraints,
    synthesis_mode: ephemeral.synthesis_mode,
    runtime: input?.runtime || ephemeral.runtime || "node"
  });
}

async function runEphemeralTool(tool, sandbox = {}) {
  const executable = compileTool(tool);
  registerExecutableTool(executable);
  const result = await executeTool({
    executable,
    input: isPlainObject(sandbox.input) ? sandbox.input : undefined,
    sandbox
  });
  const experimentValidation = await runExperimentValidation(executable, {
    experiment_inputs: sandbox.experiment_inputs || [],
    require_deterministic: sandbox.require_deterministic === true,
    deterministic_input: sandbox.deterministic_input || (isPlainObject(sandbox.input) ? sandbox.input : undefined)
  }, sandbox);
  const validation = buildValidation(executable, result);
  if ((experimentValidation.experiments || []).some((item) => !item.success)) {
    validation.success = false;
    validation.verdict = "conflicted";
    validation.rejected = true;
    validation.reasons.push("Experiment validation failed");
  }
  if (experimentValidation.deterministic && !experimentValidation.deterministic.success) {
    validation.success = false;
    validation.verdict = "conflicted";
    validation.rejected = true;
    validation.reasons.push("Determinism validation failed");
  }
  validation.experiment_validation = experimentValidation;
  result.validation = validation;
  const memory = recordToolOutcome({ tool: executable, result, validation }, { memoryPath: sandbox.memoryPath || toolMemoryPath });
  const promotion = promoteToolCandidate({
    tool: executable,
    validation,
    memory,
    memoryPath: sandbox.memoryPath || toolMemoryPath
  });
  result.promotion = promotion;
  result.audit = readAuditLog(sandbox.audit_limit || 10)
    .filter((entry) => (entry.tool_id && entry.tool_id === executable.tool_id) || entry.base_tool_id === executable.base_tool_id);
  return result;
}

