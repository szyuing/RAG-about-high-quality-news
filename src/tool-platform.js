function buildGeneratedCode(spec) {
  const runtime = chooseRuntime(spec);
  const strategy = detectStrategy(spec);
  const name = spec.name || spec.tool_id || "generated_tool";
  if (strategy === "normalized_read_generator") {
    return runtime === "python" ? buildPythonReadCode(name) : buildNodeReadCode(name);
  }
  return runtime === "python" ? buildPythonGenericCode(name) : buildNodeGenericCode(name);
}

