function readAuditLog(limit = 50) {
  const auditPath = resolveDataFile("tool-platform-audit.jsonl", "OPENSEARCH_TOOL_AUDIT_PATH");
  try {
    const lines = fs.readFileSync(auditPath, "utf8")
      .split(/\r?\n/)
      .filter(Boolean)
      .slice(-Math.max(1, Number(limit) || 50));
    return lines.map((line) => {
      try {
        return JSON.parse(line);
      } catch (_) {
        return {
          type: "malformed_audit_entry",
          raw: line
        };
      }
    });
  } catch (_) {
    return [];
  }
}

