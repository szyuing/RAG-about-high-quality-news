function hashValue(value) {
  return crypto.createHash("sha1").update(String(value || "")).digest("hex");
}

function isPlainObject(value) {
  return value !== null && typeof value === "object" && !Array.isArray(value);
}

function normalizeList(value) {
  if (!Array.isArray(value)) {
    return [];
  }
  return value.map((item) => String(item || "").trim()).filter(Boolean);
}

function makeToolId(prefix, value) {
  return `${prefix}_${hashValue(value).slice(0, 12)}`;
}

function chooseRuntime(spec = {}) {
  if (spec.runtime === "python" || spec.runtime === "node") {
    return spec.runtime;
  }
  const blob = [
    spec.goal,
    spec.description,
    spec.name,
    ...(spec.constraints || [])
  ].join(" ").toLowerCase();
  if (/python|pandas|numpy|csv|xlsx|tabular|dataframe/.test(blob)) {
    return "python";
  }
  return "node";
}

function detectStrategy(spec = {}) {
  return spec.strategy || (spec.parameters || []).some((item) => item.name === "candidate")
    ? "normalized_read_generator"
    : "generic_tool_generator";
}

function buildNodeReadCode(name) {
  return `
async function readStdin() {
  const chunks = [];
  for await (const chunk of process.stdin) {
    chunks.push(Buffer.from(chunk));
  }
  const raw = Buffer.concat(chunks).toString("utf8").trim();
  return raw ? JSON.parse(raw) : {};
}

(async () => {
  const input = await readStdin();
  const candidate = input.candidate || input.target || {};
  const title = candidate.title || candidate.url || "${name}";
  process.stdout.write(JSON.stringify({
    success: true,
    source_id: candidate.id || candidate.source_id || "generated-source",
    title,
    content_type: candidate.content_type || candidate.source_type || "web",
    source_type: candidate.source_type || candidate.content_type || "web",
    url: candidate.url || null,
    key_points: [
      \`Generated by ${name}\`,
      candidate.summary || candidate.description || "No summary provided"
    ].filter(Boolean),
    markdown: candidate.markdown || \`# \${title}\\n\\nGenerated by ${name}.\`
  }, null, 2));
})().catch((error) => {
  process.stdout.write(JSON.stringify({ success: false, error: error.message }, null, 2));
});
`;
}

function buildPythonReadCode(name) {
  return `
import json
import sys

def read_stdin():
    raw = sys.stdin.read().strip()
    return json.loads(raw) if raw else {}

try:
    payload = read_stdin()
    candidate = payload.get("candidate") or payload.get("target") or {}
    title = candidate.get("title") or candidate.get("url") or "${name}"
    result = {
        "success": True,
        "source_id": candidate.get("id") or candidate.get("source_id") or "generated-source",
        "title": title,
        "content_type": candidate.get("content_type") or candidate.get("source_type") or "web",
        "source_type": candidate.get("source_type") or candidate.get("content_type") or "web",
        "url": candidate.get("url"),
        "key_points": [item for item in [f"Generated by ${name}", candidate.get("summary"), candidate.get("description")] if item],
        "markdown": candidate.get("markdown") or f"# {title}\\n\\nGenerated by ${name}."
    }
    sys.stdout.write(json.dumps(result))
except Exception as error:
    sys.stdout.write(json.dumps({"success": False, "error": str(error)}))
`;
}

